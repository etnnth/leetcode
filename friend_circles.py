class Solution:
    def findCircleNum(self, M):


        def find(x):
            while x != parents[x]:
                x = parents[x]
            return x


        def union(x, y):
            i,j = find(x), find(y)
            if i != j:
                if rang[i] < rang[j]:
                    parents[i] = j
                else:
                    parents[j] = i
                    if rang[i] == rang[j]:
                        rang[i] +=1


        n = len(M)
        parents = {i:i for i in range(n)}
        rang =  {i:0 for i in range(n)}
        for i in range(n):
#            print(parents)
            for j in range(n):
                if M[i][j]:
                    union(i,j)

#        print(parents)
        return len(set(find(i) for i in range(n)))




